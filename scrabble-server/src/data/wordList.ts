import fs from 'fs';
import path from 'path';
import { config } from '../config';

/**
 * Load the word list from file or fallback to built-in dictionary.
 * Returns a Set of uppercase words.
 */
export async function loadWordList(): Promise<Set<string>> {
  const wordSet = new Set<string>();

  // Try reading from file
  try {
    const filePath = config.wordListPath;
    if (fs.existsSync(filePath)) {
      const content = fs.readFileSync(filePath, 'utf-8');
      const words = content
        .split(/\r?\n/)
        .map(w => w.trim().toUpperCase())
        .filter(w => w.length >= 2 && w.length <= 15 && /^[A-Z]+$/.test(w));

      for (const word of words) {
        wordSet.add(word);
      }

      console.log(`[WordList] Loaded ${wordSet.size} words from ${filePath}`);
      if (wordSet.size > 0) return wordSet;
    }
  } catch (err) {
    console.warn(`[WordList] Could not read word file: ${err}`);
  }

  // Fallback to built-in word list
  console.log('[WordList] Using built-in word list fallback');
  for (const word of BUILTIN_WORDS) {
    wordSet.add(word);
  }

  console.log(`[WordList] Loaded ${wordSet.size} words from built-in list`);
  return wordSet;
}

